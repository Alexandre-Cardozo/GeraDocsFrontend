/**
 * Ícones de linha estilo Lucide — 24×24 viewBox, stroke currentColor,
 * stroke-width 2, round caps/joins (seção "Iconography" do DS).
 * Paths idênticos aos do pacote lucide, inline para manter o bundle enxuto.
 */

export interface IconProps {
  size?: number
  strokeWidth?: number
  style?: CSSProperties
}

function makeIcon(nome: string, paths: ReactNode) {
  function Icone({ size = 16, strokeWidth = 2, style }: IconProps) {
    return (
      <svg
        width={size}
        height={size}
        viewBox="0 0 24 24"
        fill="none"
        stroke="currentColor"
        strokeWidth={strokeWidth}
        strokeLinecap="round"
        strokeLinejoin="round"
        style={style}
        aria-hidden
      >
        {paths}
      </svg>
    )
  }
  Icone.displayName = nome
  return Icone
}

